// ========== DOM 元素 ==========
const book = document.getElementById('book');
const answerText = document.getElementById('answerText');
const pageNumber = document.getElementById('pageNumber');
const flipBtn = document.getElementById('flipBtn');

// 记录翻页次数
let flipCount = 0;

// ========== 核心函数：随机获取答案 ==========
function getRandomAnswer() {
    const randomIndex = Math.floor(Math.random() * answers.length);
    return {
        answer: answers[randomIndex],
        index: randomIndex + 1  // 显示用的页码从1开始
    };
}

// ========== 翻页动作 ==========
function flipBook() {
    // 如果书已经翻开，先合上
    if (book.classList.contains('flipped')) {
        book.classList.remove('flipped');
        // 合上后延迟再翻开，更新答案
        setTimeout(() => {
            updateAnswer();
            book.classList.add('flipped');
        }, 400);
    } else {
        // 第一次翻开
        updateAnswer();
        book.classList.add('flipped');
    }
}

// ========== 更新答案 ==========
function updateAnswer() {
    const result = getRandomAnswer();
    // 给答案文字加一个淡入效果
    answerText.style.opacity = '0';
    answerText.style.transform = 'translateY(10px)';

    setTimeout(() => {
        answerText.textContent = result.answer;
        answerText.style.opacity = '1';
        answerText.style.transform = 'translateY(0)';
    }, 150);

    flipCount++;
    pageNumber.textContent = `#${flipCount}`;
}

// ========== 事件绑定 ==========
// 点击书本身翻页
book.addEventListener('click', flipBook);

// 点击按钮翻页
flipBtn.addEventListener('click', (e) => {
    e.stopPropagation(); // 防止冒泡触发书的点击
    flipBook();
});

// ========== 初始状态下预设一个答案 ==========
answerText.textContent = getRandomAnswer().answer;